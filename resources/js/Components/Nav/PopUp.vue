import { Inertia } from '@inertiajs/inertia'

export default {
    methods: {
        closePopup() {
            this.$emit('close')
        }
    },
    setup() {
        const destroy = (name) => {
            if (confirm('Are you sure?')) {
                Inertia.delete(route('users.destroy', name))
            }
        }

        return { destroy }
