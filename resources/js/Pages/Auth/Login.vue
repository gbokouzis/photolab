<template>
    <Head title="Create User" />

    <h1 class="text-3xl">Create New User</h1>

    <form @submit.prevent="submit" class="max-w-md mx-auto mt-8">
        <div class="mb-6">
            <label class="block mb-2 uppercase font-bold text-gray-700" for="Email">
                Email
            </label>
            <input v-model="form.email" class="border border-gray-400 p-2 w-full"
                type="email"
                name="email"
                id="email"
            >
            <div v-if="form.errors.email" v-text="form.errors.email" 
                class="text-red-500 text-xs mt-1" />
        </div>

        <div class="mb-6">
            <label class="block mb-2 uppercase font-bold text-gray-700" for="Password">
                Password
            </label>
            <input v-model="form.password" class="border border-gray-400 p-2 w-full"
                type="password"
                name="password"
                id="password"
            >
        </div>
            <div v-if="form.errors.password" v-text="form.errors.password" 
                class="text-red-500 text-xs mt-1" />

        <div class="mp-6">
            <button 
                type="submit" 
                class="bg-blue-400 text-white rounded py-2 px-4 hover:bg-blue-500"
            >
                Submit    
            </button>    
        </div> 

    </form>

</template>

<script setup>
import { useForm } from "@inertiajs/inertia-vue3";

let form = useForm({
    email: '',
    password:'',
})

let submit = () => {
    form.post('/login', form)
}

</script>

